//
//   Codeforces - A. Digits Sum
//   https://codeforces.com/problemset/problem/1553/A
//

#include <bits/stdc++.h>
using namespace std;

void solve() {
    int t;
    cin >> t;
    while(t--) {
    	int n;
    	cin >> n;

    	cout << (n+1)/10 << "\n";
    }
}

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);
    solve();
    return 0;
}