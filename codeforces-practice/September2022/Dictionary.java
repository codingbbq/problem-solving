    Map<String, Integer> generateMap() {
        Map<String, Integer> map = new HashMap<>();
        int index = 0;
        for(int i = 0; i < 26; i++) {
            for(int j = 0; j < 26; j++) {
                if(i != j) {
                    String x = Character.toString((char)(i + 97)) + "" + Character.toString((char)(j + 97));
                    map.put(x, ++index);
                }
            }
        }
        return map;
    }


