//
//   Codeforces - A. Prison Break
//   https://codeforces.com/problemset/problem/1482/A
//

#include <bits/stdc++.h>
using namespace std;

void solve() {
    int t;
    cin >> t;

    while(t--) {
    	int a, b;
    	cin >> a >> b;

    	cout << a*b << "\n";
    }
}

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);
    solve();
    return 0;
}